import { runOracleQuery } from './oracledb';
import { mergeShiftConfig, STAFF_SHIFT_DEFAULTS } from './staff-shift-config';

const CONFIG_KEY = 'default';
const initialized = globalThis.__staffShiftOracleInit || { promise: null, done: false };
globalThis.__staffShiftOracleInit = initialized;

function jsDate(value = new Date()) {
  return value instanceof Date ? value : new Date(value);
}

function parseJson(value, fallback) {
  if (!value) return fallback;
  if (typeof value === 'object') return value;
  try {
    return JSON.parse(value);
  } catch {
    return fallback;
  }
}

function minutesBetween(start, end = new Date()) {
  if (!start) return 0;
  return Math.max(0, Math.floor((jsDate(end).getTime() - jsDate(start).getTime()) / 60000));
}

function normalizeRow(row = {}) {
  const out = {};
  for (const [key, value] of Object.entries(row)) {
    out[key.toLowerCase()] = value;
  }
  return out;
}

function normalizeRows(rows = []) {
  return rows.map(normalizeRow);
}

function shiftWorkedMinutes(shift, now = new Date()) {
  if (!shift) return 0;
  const row = normalizeRow(shift);
  const end = row.ended_at || now;
  const total = minutesBetween(row.started_at, end);
  return Math.max(0, total - Number(row.break_minutes || 0));
}

function getWaveBounds(config, now = new Date()) {
  const anchorMs = new Date(config.waveAnchor).getTime();
  const durationMs = Number(config.waveDurationDays || 7) * 24 * 60 * 60 * 1000;
  const currentMs = now.getTime();
  const waveIndex = Math.max(0, Math.floor((currentMs - anchorMs) / durationMs));
  return {
    number: waveIndex + 1,
    startsAt: new Date(anchorMs + waveIndex * durationMs),
    endsAt: new Date(anchorMs + (waveIndex + 1) * durationMs),
  };
}

async function ignoreAlreadyExists(sql) {
  try {
    await runOracleQuery(sql, {}, { autoCommit: true });
  } catch (error) {
    if (error?.errorNum === 955 || String(error?.message || '').includes('ORA-00955')) return;
    throw error;
  }
}

async function ensureStaffShiftTables() {
  if (initialized.done) return;
  if (initialized.promise) return initialized.promise;

  initialized.promise = (async () => {
    await ignoreAlreadyExists(`
      CREATE TABLE staff_shift_config (
        config_key VARCHAR2(64) PRIMARY KEY,
        config_json CLOB CHECK (config_json IS JSON),
        updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
      )
    `);

    await ignoreAlreadyExists(`
      CREATE TABLE staff_shifts (
        id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        wave_number NUMBER NOT NULL,
        discord_id VARCHAR2(32) NOT NULL,
        discord_name VARCHAR2(255),
        started_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
        ended_at TIMESTAMP NULL,
        break_started_at TIMESTAMP NULL,
        break_minutes NUMBER DEFAULT 0 NOT NULL,
        status VARCHAR2(24) DEFAULT 'active' NOT NULL
          CHECK (status IN ('active', 'break', 'ended', 'force_ended')),
        ended_by VARCHAR2(32),
        end_reason VARCHAR2(255),
        created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
        updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
      )
    `);

    await ignoreAlreadyExists(`CREATE INDEX idx_staff_shifts_user_wave ON staff_shifts (discord_id, wave_number)`);
    await ignoreAlreadyExists(`CREATE INDEX idx_staff_shifts_status ON staff_shifts (status)`);
    await ignoreAlreadyExists(`CREATE INDEX idx_staff_shifts_started ON staff_shifts (started_at)`);

    await ignoreAlreadyExists(`
      CREATE TABLE staff_shift_breaks (
        id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        shift_id NUMBER NOT NULL,
        started_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
        ended_at TIMESTAMP NULL,
        duration_minutes NUMBER DEFAULT 0 NOT NULL,
        ended_by VARCHAR2(32)
      )
    `);
    await ignoreAlreadyExists(`CREATE INDEX idx_staff_breaks_shift ON staff_shift_breaks (shift_id)`);

    await ignoreAlreadyExists(`
      CREATE TABLE staff_shift_adjustments (
        id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        wave_number NUMBER NOT NULL,
        discord_id VARCHAR2(32) NOT NULL,
        shift_id NUMBER NULL,
        minutes_delta NUMBER NOT NULL,
        reason VARCHAR2(500),
        actor_id VARCHAR2(32) NOT NULL,
        created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
      )
    `);
    await ignoreAlreadyExists(`CREATE INDEX idx_staff_adj_user_wave ON staff_shift_adjustments (discord_id, wave_number)`);
    await ignoreAlreadyExists(`CREATE INDEX idx_staff_adj_shift ON staff_shift_adjustments (shift_id)`);

    await ignoreAlreadyExists(`
      CREATE TABLE staff_player_logs (
        id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        wave_number NUMBER NOT NULL,
        staff_discord_id VARCHAR2(32) NOT NULL,
        staff_name VARCHAR2(255),
        roblox_user_id VARCHAR2(32),
        roblox_username VARCHAR2(255) NOT NULL,
        action VARCHAR2(24) NOT NULL
          CHECK (action IN ('warn', 'kick', 'ban', 'bolo', 'custom')),
        reason VARCHAR2(1000) NOT NULL,
        source VARCHAR2(24) DEFAULT 'website' NOT NULL
          CHECK (source IN ('website', 'discord')),
        is_in_game NUMBER(1) DEFAULT 0 NOT NULL,
        created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
      )
    `);
    await ignoreAlreadyExists(`CREATE INDEX idx_staff_logs_created ON staff_player_logs (created_at)`);
    await ignoreAlreadyExists(`CREATE INDEX idx_staff_logs_target ON staff_player_logs (roblox_username)`);
    await ignoreAlreadyExists(`CREATE INDEX idx_staff_logs_staff ON staff_player_logs (staff_discord_id)`);

    await ignoreAlreadyExists(`
      CREATE TABLE staff_quota_archives (
        wave_number NUMBER PRIMARY KEY,
        started_at TIMESTAMP NOT NULL,
        ended_at TIMESTAMP NOT NULL,
        archived_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
        archive_json CLOB CHECK (archive_json IS JSON)
      )
    `);

    await runOracleQuery(
      `MERGE INTO staff_shift_config target
       USING (SELECT :configKey AS config_key, :configJson AS config_json FROM dual) source
       ON (target.config_key = source.config_key)
       WHEN NOT MATCHED THEN
         INSERT (config_key, config_json) VALUES (source.config_key, source.config_json)`,
      { configKey: CONFIG_KEY, configJson: JSON.stringify(STAFF_SHIFT_DEFAULTS) },
      { autoCommit: true }
    );

    initialized.done = true;
  })().finally(() => {
    initialized.promise = null;
  });

  return initialized.promise;
}

async function getStaffShiftConfig() {
  await ensureStaffShiftTables();
  const result = await runOracleQuery(
    'SELECT config_json FROM staff_shift_config WHERE config_key = :configKey',
    { configKey: CONFIG_KEY }
  );
  const rows = normalizeRows(result.rows || []);
  return mergeShiftConfig(parseJson(rows[0]?.config_json, {}));
}

async function updateStaffShiftConfig(nextConfig) {
  await ensureStaffShiftTables();
  const merged = mergeShiftConfig(nextConfig || {});
  await runOracleQuery(
    `UPDATE staff_shift_config
     SET config_json = :configJson, updated_at = SYSTIMESTAMP
     WHERE config_key = :configKey`,
    { configKey: CONFIG_KEY, configJson: JSON.stringify(merged) },
    { autoCommit: true }
  );
  return merged;
}

async function getCurrentWave(config) {
  const resolved = config || await getStaffShiftConfig();
  return getWaveBounds(resolved);
}

async function getMaxShiftWave() {
  const result = await runOracleQuery('SELECT MAX(wave_number) AS max_wave FROM staff_shifts');
  return Number(normalizeRows(result.rows || [])[0]?.max_wave || 0);
}

async function archiveFinishedWaves() {
  await ensureStaffShiftTables();
  const config = await getStaffShiftConfig();
  const currentWave = getWaveBounds(config);
  const maxWave = await getMaxShiftWave();

  for (let waveNumber = 1; waveNumber < currentWave.number && waveNumber <= maxWave; waveNumber++) {
    const existing = await runOracleQuery(
      'SELECT wave_number FROM staff_quota_archives WHERE wave_number = :waveNumber',
      { waveNumber }
    );
    if ((existing.rows || []).length) continue;

    const anchorMs = new Date(config.waveAnchor).getTime();
    const durationMs = Number(config.waveDurationDays || 7) * 24 * 60 * 60 * 1000;
    const startedAt = new Date(anchorMs + (waveNumber - 1) * durationMs);
    const endedAt = new Date(anchorMs + waveNumber * durationMs);
    const leaderboard = await getLeaderboard({ waveNumber, limit: 500, includeActive: false });

    await runOracleQuery(
      `INSERT INTO staff_quota_archives (wave_number, started_at, ended_at, archive_json)
       VALUES (:waveNumber, :startedAt, :endedAt, :archiveJson)`,
      {
        waveNumber,
        startedAt,
        endedAt,
        archiveJson: JSON.stringify({ waveNumber, startedAt, endedAt, leaderboard }),
      },
      { autoCommit: true }
    );
  }
}

async function getActiveShift(discordId) {
  await ensureStaffShiftTables();
  const result = await runOracleQuery(
    `SELECT * FROM staff_shifts
     WHERE discord_id = :discordId AND status IN ('active', 'break')
     ORDER BY started_at DESC FETCH FIRST 1 ROWS ONLY`,
    { discordId }
  );
  return normalizeRows(result.rows || [])[0] || null;
}

async function getShiftById(id) {
  const result = await runOracleQuery('SELECT * FROM staff_shifts WHERE id = :id', { id });
  return normalizeRows(result.rows || [])[0] || null;
}

async function startShift({ discordId, discordName }) {
  await archiveFinishedWaves();
  const active = await getActiveShift(discordId);
  if (active) return active;

  const wave = await getCurrentWave();
  const result = await runOracleQuery(
    `INSERT INTO staff_shifts (wave_number, discord_id, discord_name, started_at, status)
     VALUES (:waveNumber, :discordId, :discordName, SYSTIMESTAMP, 'active')
     RETURNING id INTO :id`,
    {
      waveNumber: wave.number,
      discordId,
      discordName,
      id: { dir: 3003, type: 2010 },
    },
    { autoCommit: true }
  );
  return getShiftById(result.outBinds.id[0]);
}

async function endShift({ discordId, actorId, force = false, reason = null }) {
  await ensureStaffShiftTables();
  const active = await getActiveShift(discordId);
  if (!active) return null;

  let breakMinutes = Number(active.break_minutes || 0);
  if (active.status === 'break' && active.break_started_at) {
    const extra = minutesBetween(active.break_started_at);
    breakMinutes += extra;
    await runOracleQuery(
      `UPDATE staff_shift_breaks
       SET ended_at = SYSTIMESTAMP, duration_minutes = :extra, ended_by = :actorId
       WHERE shift_id = :shiftId AND ended_at IS NULL`,
      { extra, actorId, shiftId: active.id },
      { autoCommit: true }
    );
  }

  await runOracleQuery(
    `UPDATE staff_shifts
     SET ended_at = SYSTIMESTAMP,
         status = :status,
         break_started_at = NULL,
         break_minutes = :breakMinutes,
         ended_by = :actorId,
         end_reason = :reason,
         updated_at = SYSTIMESTAMP
     WHERE id = :shiftId`,
    {
      status: force ? 'force_ended' : 'ended',
      breakMinutes,
      actorId,
      reason,
      shiftId: active.id,
    },
    { autoCommit: true }
  );

  return getShiftById(active.id);
}

async function startBreak({ discordId }) {
  await ensureStaffShiftTables();
  const active = await getActiveShift(discordId);
  if (!active || active.status !== 'active') return active;

  await runOracleQuery(
    `UPDATE staff_shifts
     SET status = 'break', break_started_at = SYSTIMESTAMP, updated_at = SYSTIMESTAMP
     WHERE id = :shiftId`,
    { shiftId: active.id },
    { autoCommit: true }
  );
  await runOracleQuery(
    'INSERT INTO staff_shift_breaks (shift_id, started_at) VALUES (:shiftId, SYSTIMESTAMP)',
    { shiftId: active.id },
    { autoCommit: true }
  );

  return getActiveShift(discordId);
}

async function endBreak({ discordId, actorId }) {
  await ensureStaffShiftTables();
  const active = await getActiveShift(discordId);
  if (!active || active.status !== 'break') return active;
  const extra = minutesBetween(active.break_started_at);

  await runOracleQuery(
    `UPDATE staff_shifts
     SET status = 'active',
         break_started_at = NULL,
         break_minutes = break_minutes + :extra,
         updated_at = SYSTIMESTAMP
     WHERE id = :shiftId`,
    { extra, shiftId: active.id },
    { autoCommit: true }
  );
  await runOracleQuery(
    `UPDATE staff_shift_breaks
     SET ended_at = SYSTIMESTAMP, duration_minutes = :extra, ended_by = :actorId
     WHERE shift_id = :shiftId AND ended_at IS NULL`,
    { extra, actorId, shiftId: active.id },
    { autoCommit: true }
  );

  return getActiveShift(discordId);
}

async function deleteExpiredInGameLogs(config) {
  await runOracleQuery(
    `DELETE FROM staff_player_logs
     WHERE is_in_game = 1
       AND created_at < ADD_MONTHS(SYSTIMESTAMP, -:months)`,
    { months: Number(config.inGameLogRetentionMonths || 2) },
    { autoCommit: true }
  );
}

async function addStaffLog({ staffDiscordId, staffName, robloxUserId, robloxUsername, action, reason, source = 'website', isInGame = false }) {
  await archiveFinishedWaves();
  const config = await getStaffShiftConfig();
  await deleteExpiredInGameLogs(config);
  const wave = getWaveBounds(config);

  const result = await runOracleQuery(
    `INSERT INTO staff_player_logs
      (wave_number, staff_discord_id, staff_name, roblox_user_id, roblox_username, action, reason, source, is_in_game)
     VALUES
      (:waveNumber, :staffDiscordId, :staffName, :robloxUserId, :robloxUsername, :action, :reason, :source, :isInGame)
     RETURNING id INTO :id`,
    {
      waveNumber: wave.number,
      staffDiscordId,
      staffName,
      robloxUserId: robloxUserId || null,
      robloxUsername,
      action,
      reason,
      source,
      isInGame: isInGame ? 1 : 0,
      id: { dir: 3003, type: 2010 },
    },
    { autoCommit: true }
  );

  const rows = await runOracleQuery('SELECT * FROM staff_player_logs WHERE id = :id', { id: result.outBinds.id[0] });
  return normalizeRows(rows.rows || [])[0] || null;
}

async function getRecentStaffLogs({ minutes = 60, limit = 80, query = null } = {}) {
  await ensureStaffShiftTables();
  const binds = { minutes: Number(minutes || 60), limit: Number(limit || 80) };
  let where = "created_at >= (SYSTIMESTAMP - NUMTODSINTERVAL(:minutes, 'MINUTE'))";
  if (query) {
    where += ' AND (LOWER(roblox_username) LIKE :query OR LOWER(staff_name) LIKE :query OR staff_discord_id = :exactQuery)';
    binds.query = `%${String(query).toLowerCase()}%`;
    binds.exactQuery = String(query);
  }
  const result = await runOracleQuery(
    `SELECT * FROM staff_player_logs WHERE ${where} ORDER BY created_at DESC FETCH FIRST :limit ROWS ONLY`,
    binds
  );
  return normalizeRows(result.rows || []);
}

async function getAllActiveShifts() {
  await ensureStaffShiftTables();
  const result = await runOracleQuery(
    `SELECT * FROM staff_shifts
     WHERE status IN ('active', 'break')
     ORDER BY started_at ASC`
  );
  return normalizeRows(result.rows || []).map(row => ({
    ...row,
    workedMinutes: shiftWorkedMinutes(row),
    onBreak: row.status === 'break',
  }));
}

async function getUserShiftSummary(discordId, waveNumber = null) {
  await ensureStaffShiftTables();
  const wave = waveNumber || (await getCurrentWave()).number;
  const shiftResult = await runOracleQuery(
    'SELECT * FROM staff_shifts WHERE discord_id = :discordId AND wave_number = :waveNumber ORDER BY started_at DESC',
    { discordId, waveNumber: wave }
  );
  const adjustmentResult = await runOracleQuery(
    'SELECT * FROM staff_shift_adjustments WHERE discord_id = :discordId AND wave_number = :waveNumber ORDER BY created_at DESC',
    { discordId, waveNumber: wave }
  );
  const shifts = normalizeRows(shiftResult.rows || []);
  const adjustments = normalizeRows(adjustmentResult.rows || []);
  const totalShiftMinutes = shifts.reduce((sum, shift) => sum + shiftWorkedMinutes(shift), 0);
  const adjustedMinutes = adjustments.reduce((sum, item) => sum + Number(item.minutes_delta || 0), 0);

  return {
    discordId,
    waveNumber: wave,
    shifts,
    adjustments,
    totalShiftMinutes,
    adjustedMinutes,
    totalMinutes: Math.max(0, totalShiftMinutes + adjustedMinutes),
    activeShift: shifts.find(shift => ['active', 'break'].includes(shift.status)) || null,
  };
}

async function getLeaderboard({ waveNumber = null, limit = 100, includeActive = true } = {}) {
  await ensureStaffShiftTables();
  const wave = waveNumber || (await getCurrentWave()).number;
  const shiftResult = await runOracleQuery(
    'SELECT * FROM staff_shifts WHERE wave_number = :waveNumber',
    { waveNumber: wave }
  );
  const adjustmentResult = await runOracleQuery(
    'SELECT * FROM staff_shift_adjustments WHERE wave_number = :waveNumber',
    { waveNumber: wave }
  );
  const byUser = new Map();

  for (const shift of normalizeRows(shiftResult.rows || [])) {
    const item = byUser.get(shift.discord_id) || {
      discordId: shift.discord_id,
      discordName: shift.discord_name,
      shiftMinutes: 0,
      adjustedMinutes: 0,
      totalMinutes: 0,
      shifts: 0,
      active: false,
      onBreak: false,
    };
    if (includeActive || !['active', 'break'].includes(shift.status)) {
      item.shiftMinutes += shiftWorkedMinutes(shift);
      item.shifts += 1;
    }
    item.active ||= ['active', 'break'].includes(shift.status);
    item.onBreak ||= shift.status === 'break';
    byUser.set(shift.discord_id, item);
  }

  for (const adjustment of normalizeRows(adjustmentResult.rows || [])) {
    const item = byUser.get(adjustment.discord_id) || {
      discordId: adjustment.discord_id,
      discordName: adjustment.discord_id,
      shiftMinutes: 0,
      adjustedMinutes: 0,
      totalMinutes: 0,
      shifts: 0,
      active: false,
      onBreak: false,
    };
    item.adjustedMinutes += Number(adjustment.minutes_delta || 0);
    byUser.set(adjustment.discord_id, item);
  }

  return [...byUser.values()]
    .map(item => ({ ...item, totalMinutes: Math.max(0, item.shiftMinutes + item.adjustedMinutes) }))
    .sort((a, b) => b.totalMinutes - a.totalMinutes)
    .slice(0, Number(limit || 100));
}

async function adjustQuota({ discordId, shiftId = null, minutesDelta, reason, actorId }) {
  await ensureStaffShiftTables();
  const wave = await getCurrentWave();
  await runOracleQuery(
    `INSERT INTO staff_shift_adjustments
      (wave_number, discord_id, shift_id, minutes_delta, reason, actor_id)
     VALUES (:waveNumber, :discordId, :shiftId, :minutesDelta, :reason, :actorId)`,
    {
      waveNumber: wave.number,
      discordId,
      shiftId: shiftId || null,
      minutesDelta: Number(minutesDelta),
      reason: reason || null,
      actorId,
    },
    { autoCommit: true }
  );
  return getUserShiftSummary(discordId, wave.number);
}

async function getQuotaArchive(waveNumber) {
  await ensureStaffShiftTables();
  const result = await runOracleQuery(
    'SELECT * FROM staff_quota_archives WHERE wave_number = :waveNumber',
    { waveNumber: Number(waveNumber) }
  );
  const row = normalizeRows(result.rows || [])[0];
  if (!row) return null;
  return {
    ...row,
    archive: parseJson(row.archive_json, null),
  };
}

